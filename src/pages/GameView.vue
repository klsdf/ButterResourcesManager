    <!-- 编辑游戏对话框 -->
    <div v-if="showEditDialog" class="modal-overlay" @click="closeEditGameDialog">
      <div class="modal-content" @click.stop>
        <div class="modal-header">
          <h3>编辑游戏</h3>
