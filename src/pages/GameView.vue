      // 防止重复截图：检查是否正在截图或距离上次截图时间太短
      const now = Date.now()
      if (this.isScreenshotInProgress || (now - this.lastScreenshotTime < 1000)) {
        console.log('截图请求被忽略：正在截图或距离上次截图时间太短')
        return
      }
      
      this.isScreenshotInProgress = true
      this.lastScreenshotTime = now
      
