        <div class="modal-body">
          <FormField
            label="游戏名称"
            type="text"
            v-model="editGameForm.name"
            placeholder="输入游戏名称"
          />
          <FormField
            label="开发商"
            type="text"
            v-model="editGameForm.developer"
            placeholder="输入开发商名称"
          />
          <FormField
            label="发行商"
